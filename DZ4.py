'''Задание 1
Пользователь вводит с клавиатуры номер дня недели(1-7). Необходимо вывести на экран названия дня недели.
Например, если 1, то на экране надпись понедельник, 2 - вторник и т.д. '''

x=int(input('Введите число - номер дня недели: '))
if x==1: print(x,' - Понедельник')
if x==2: print(x,' - Вторник')
if x==3: print(x,' - Среда')
if x==4: print(x,' - Четверг')
if x==5: print(x,' - Пятница')
if x==6: print(x,' - Суббота')
if x==7: print(x,' -  Воскресенье')


