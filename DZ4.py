'''Задание 3
Пользователь вводит с клавиатуры число. Если число больше нуля нужно вывести надпись «Numberis positive»,
если меньше нуля «Number is negative», если равно нулю «Number is equal to zero»'''

x=int(input('Введите число: '))
if x>0: print(x,'Number is positive')
elif x<0: print(x,'Number is negative')
elif x==0: print(x,'Number is equal to zero')


'''Задание 4
Пользователь вводит два числа. Определить, равны ли эти числа, и, если нет, вывести их на экран в порядке
возрастания.'''

x1=int(input('Введите первое число: '))
x2=int(input('Введите второе число: '))
if x1!=x2: print(min(x1,x2), max(x1,x2))
